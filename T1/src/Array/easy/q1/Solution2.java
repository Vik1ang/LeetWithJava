package Array.easy.q1;

/**
 * @author Vikiang
 * @URL https://leetcode-cn.com/problems/two-sum/
 */
public class Solution2 {
    public int[] twoSum(int[] nums, int target) {
        int n = nums.length;
        for (int i = 0; i < n; i++) {
            for (int j = i + 1; j < n; j++) {
                if (nums[i] + nums[j] == target) {
                    return new int[]{i, j};
                }
            }
        }
        return new int[0];
    }
}
